package lindy.tabBeginner

import kotlinx.html.p
import lindy.LindyBeginnerSections
import tabs.SectionedTabDsl

fun LindyBeginnerSections.positioning(dsl: SectionedTabDsl) {
    with(dsl) {
        section("Positioning") {
            content {
                /*
                <p>
                    These are some simple techniques to change your positions/places.
                    It usually involves a pulling force and a few traveling steps, and ending up standing on each other's original spot.
                </p>
                 */
            }
            subSection("Open-Closed-Transition") {
                // =====================================================================================================
                content {
                    p { +"TODO" }
                    /*
                                    <p>
                    In order to go from open to closed-position, you can continue to pull backwards (by stepping backwards) after the rock-step.
                    This should be enough for the follower to come closer, and with the leader's right arm reached open, it should be visually enough to make the intention clear.
                    Otherwise, the follower can do that herself at any time; no need to have a too rigid &quot;leader leading only&quot; attitude.
                </p>
                <p>
                    Another way is to do a so-called <b>fake change places</b>.
                    The leader raises his arm (as if looking at his watch), but stays on the spot (not changing his place), while the follower continues doing a change places.
                    He reaches early for her back to indicate moving into a closed position.<br/>
                    There is something awkward happening with the arm, as she has to move it around his shoulder. For safety reasons, lead from the hand, raising it high up, and not from the elbow, to not hit your lead in his face with your elbow.
                </p>
                     */
                }
            }
            subSection("Pass-By") {
                // =====================================================================================================
                content {
                    p { +"TODO" }
                    /*
                                    <p>
                    Also called &quot;change positions&quot; or &quot;change places&quot; -nomen est omen.
                    We start in an <b>open position</b> and keep, for example, an async (opposite direction) basic-step (6-count feels more comfortable, but 8-count is possible as well).
                    The easiest way to play around here is to simply indicate a change by <b>moving the arm</b> a bit to the left or right, just as if we would indicate a travel-step.
                    Try to also switch into <b>crossed-hand</b> position.
                </p>
                <p>
                    The leader <b>raises his arm</b> straight up, internally rotating as if he would look at his watch on the second count/quick-step.
                    The follower goes underneath that arm and rotates 180 degrees CCW.<br/>
                    Remember to do small steps (otherwise end up too far away from each other), and to not cross your steps.
                </p>
                <p>
                    Similarly, the <b>leader</b> can lift his arm more to the left and walk underneath it himself, turning CCW (with the back towards her), while the follower is still facing him.
                </p>

                <div class="videoContainer">
                    <iframe src="https://www.youtube.com/embed/aXxqsY45GD8?si=MXHj3KMjDVJ8P9GF" title="Beginners - Change places"
                            allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share" referrerpolicy="strict-origin-when-cross-origin" allowfullscreen></iframe>
                    <p class="caption">Freely changing places in the open position</p>
                </div>

                <p>
                    A bit more advanced: When trying to pull back your follower, you can at the very last moment, lift your arm and turn her in <b>the other direction</b> (seems a bit counter-intuitive and surprising at first).
                </p>

                <div class="videoContainer">
                    <iframe src="https://www.youtube.com/embed/JP3ZBYMFHcA?si=MR65UK7G29WcRf_B" title="Beginners - Repeater"
                            allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture; web-share" referrerpolicy="strict-origin-when-cross-origin" allowfullscreen></iframe>
                    <p class="caption">Going from an open to a closed position by catching the follower during a turn</p>
                </div>
                     */
                }
            }
            subSection("Around the World") {
                // =====================================================================================================
                content {
                    p { +"TODO" }
                    /*
                <p>
                    In open-position (6-count), we start like doing a simple pass-by, leader going a bit more to the left.
                    The right hand goes over the arms, the left hand letting go of the contact and rotating CCW.
                    Just as with the Soltinho in Zouk, the follower's hand slides over the leader's body (like a belt).
                </p>
                <p>
                    Catching the hand contact again can be a bit tricky, but by keeping the arms up, and sliding and rotating a bit it should work.
                    Alternatively, you can also catch the hand from the inside, ending up in a &quot;wrong&quot; hand position but then quickly fix it with a change places rotation.
                </p>
                     */
                }
            }
            subSection("Mess Around") {
                // =====================================================================================================
                content {
                    p { +"TODO" }
                    /*
                        <p>
                    After the starting rock-step, break the footwork, connect the hip of your partner, and draw a circle (preferably CCW as this seems easier).
                    To start again (exit), simply enter with a rock-step.
                </p>
                     */
                }
            }
        }
    }
}
